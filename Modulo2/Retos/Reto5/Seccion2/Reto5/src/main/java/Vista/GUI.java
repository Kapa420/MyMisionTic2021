    private void ButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGuardarActionPerformed
        this.LabelRta.setText("");
        String nombre = this.TextFieldRegistrarNombre.getText();
        String cantidad = this.TextFieldRegistrarCantidad.getText();
        String categoria = this.ComboBoxCategoria.getSelectedItem().toString();
        String precio = this.TextFieldRegistrarPrecio.getText();
        if (nombre.length() == 0
                || cantidad.length() == 0
                || precio.length() == 0
            ) {
            this.LabelRta.setText("Ingrese valores validos");
        } else {
            try {
                Controller.registrarProducto(nombre, Integer.valueOf(cantidad), categoria, Double.valueOf(precio));
                this.LabelRta.setText("Producto registrado con exito");
                this.TextFieldRegistrarCantidad.setText(null);
                this.TextFieldRegistrarNombre.setText(null);
                this.TextFieldRegistrarPrecio.setText(null);
            } catch (SQLException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                this.LabelRta.setText("Producto no registrado. ERROR");
            }
        }
    }//GEN-LAST:event_ButtonGuardarActionPerformed


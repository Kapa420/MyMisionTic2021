    private void ButtonConsultarConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConsultarConsultarActionPerformed
        String idString = this.TextFieldIDConsultar.getText();
        DefaultTableModel modelo = (DefaultTableModel) this.TablaDB.getModel();
        modelo.setRowCount(0);
        if (idString.length() == 0) {
            List<Producto> listaproductos = Controller.listarProductos();
            for (int i = 0; i < listaproductos.size(); i++) {
                Producto pr = listaproductos.get(i);
                modelo.insertRow(
                        i, new Object[]{
                            pr.getID(), pr.getNombre(), pr.getCategoria(),
                            pr.getCantidad(), pr.getPrecio()
                        }
                );
            }

        } else {
            Producto pro = Controller.obtenerProducto(Integer.valueOf(idString));
            modelo.insertRow(
                    0, new Object[]{
                        pro.getID(), pro.getNombre(), pro.getCategoria(),
                        pro.getCantidad(), pro.getPrecio()
                    }
            );
        }

    }//GEN-LAST:event_ButtonConsultarConsultarActionPerformed

    private void TablaDBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDBMouseClicked
        JTable table = (JTable) evt.getSource();
        int row = table.rowAtPoint(evt.getPoint());
        DefaultTableModel model = (DefaultTableModel) this.TablaDB.getModel();
        String ID = String.valueOf(model.getValueAt(row, 0));
        String Nombre = String.valueOf(model.getValueAt(row, 1));
        String Categoria = String.valueOf(model.getValueAt(row, 2));
        String Cantidad = String.valueOf(model.getValueAt(row, 3));
        String Precio = String.valueOf(model.getValueAt(row, 4));
        
        this.TextFieldIDConsultar.setText(ID);
        this.TextFieldNombreConsultar.setText(Nombre);
        this.TextFieldPrecioConsultar.setText(Precio);
        this.TextFieldCantidadConsultar.setText(Cantidad);
        this.ComboBoxCategoriaConsultar.setSelectedItem(Categoria);
    }//GEN-LAST:event_TablaDBMouseClicked

    private void ButtonEliminarConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarConsultarActionPerformed
        String ID = this.TextFieldIDConsultar.getText();
        if (ID.length() == 0)
        {
            this.LabelRtaConsultar.setText("Ingrese datos validos");
        }else{
            Controller.borrarProducto(Integer.valueOf(ID));
            this.LabelRtaConsultar.setText("Eliminacion Exitosa!");
        }
        this.TextFieldCantidadConsultar.setText(null);
        this.TextFieldNombreConsultar.setText(null);
        this.TextFieldPrecioConsultar.setText(null);
        this.TextFieldIDConsultar.setText(null);
    }//GEN-LAST:event_ButtonEliminarConsultarActionPerformed

